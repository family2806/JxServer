 function GetValueByRandom(tbAward)
 	local nRanVar =random(1,100);
	local num = 0; row = 0
	for row=1,getn(tbAward) do
		num = num + tbAward[row].nRate;
		if nRanVar <= num then
			return tbAward[row].nExp_tl, tbAward[row].nValue
		end
	end
 end
 
 function ShopTieuLong()
 	if (Event1000_ActiveDate() ~= 1) then
 		Talk(1,"","1000年升龙活动已结束了!")
 		return
 	end
 	AskClientForNumber("GetTieuLong",1,50,"<#>输入需要购买的数量")
 end
 
 function GetTieuLong(nCount)
 	local nSum = nCount * 50
 	if (GetEnergy() < nSum) then
 		Talk(1,"", "大侠不足精炼石，请再检查")
 		return
 	end 
 	ReduceEnergy(nSum)
 	local nItemIndex = AddStackItem(nCount, 6, 1, 30049, 1, 0, 0)
	ITEM_SetExpiredTime(nItemIndex, 20101025);
	SyncItem(nItemIndex)
	Msg2Player(format("大侠获得%d 升天令(小)", nCount))
	WriteLog(format(date("%Y%m%d %H%M%S").."\t".."Event 1000 Nm Thng Long"..GetAccount().."\t"..GetName().."\t".."购买成功 %d 小龙",nCount))
 end
 
 function Event1000_ActiveDate()
		local nDate = tonumber(GetLocalDate("%Y%m%d"))
		if (nDate > 20101024) then
			return 0
		end
		return 1
end
