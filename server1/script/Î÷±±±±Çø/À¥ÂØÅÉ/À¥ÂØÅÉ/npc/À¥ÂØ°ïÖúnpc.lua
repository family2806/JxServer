function task_get()
	UTask_kl = GetTask(9)
	if (UTask_kl < 10*256) then	-- 未入门
		Say("你现在还未入门，要入门的话就到新手村见本派的接引弟子就可以了",4,"继续了解现在任务/T_enroll","了解别的任务/T_all","了解别的问题/main","不问了!/nothing")
	elseif (UTask_kl >= 10*256) and (UTask_kl < 20*256) then
		Say("你现在进行的是采药任务.",4,"继续了解现在任务/T_L10","了解别的任务/T_all","了解别的问题/main","不问了!/nothing")
	elseif (UTask_kl >= 20*256) and (UTask_kl < 30*256) then
		Say("你现在进行的是骆驼头骨任务.",4,"继续了解现在任务/T_L20","了解别的任务/T_all","了解别的问题/main","不问了!/nothing")
	elseif (UTask_kl >= 30*256) and (UTask_kl < 40*256) then
		Say("你现在进行的是夜明珠任务.",4,"继续了解现在任务/T_L30","了解别的任务/T_all","了解别的问题/main","不问了!/nothing")
	elseif (UTask_kl >= 40*256) and (UTask_kl < 50*256) then
		Say("你现在进行的是血魂神剑任务.",4,"继续了解现在任务/T_L40","了解别的任务/T_all","了解别的问题/main","不问了!/nothing")
	elseif (UTask_kl >= 50*256) and (UTask_kl < 60*256) then
		Say("你现在进行的是雪怪任务.",4,"继续了解现在任务/T_L50","了解别的任务/T_all","了解别的问题/main","不问了!/nothing")
	elseif (UTask_kl >= 60*256) and (UTask_kl < 70*256) then
		Say("你现在进行的是五彩石任务.",4,"继续了解现在任务/T_L60","了解别的任务/T_all","了解别的问题/main","不问了!/nothing")
	elseif (UTask_kl >= 70*256) and (UTask_kl < 80*256) then
		Say("你现在进行的是重返师门任务.",4,"继续了解现在任务/T_return","了解别的任务/T_all","了解别的问题/main","不问了!/nothing")
	else
		Say("你现在可以自由进出门派，暂时没有新任务!",3,"了解别的任务/T_all","了解别的问题/main","不问了!/nothing")
	end
end

