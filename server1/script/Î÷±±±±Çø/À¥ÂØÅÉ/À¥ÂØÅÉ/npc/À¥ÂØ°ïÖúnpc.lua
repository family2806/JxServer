function main(sel)
	UTask_kl = GetTask(9)
	player_faction = GetFaction()
	if (player_faction == "kunlun") then		-- 本门弟子
		RestoreLife()		-- 本门免费治疗
		RestoreMana()
		RestoreStamina()
		Say("老夫在本门几十年了，见证了太多的变迁，知道该说的说，不该说的就不要去刨根问底",4,"了解本门事务/faction","了解各位兄长/member","了解任务/task_get","不问了!/nothing")
	elseif (UTask_kl >= 70*256) then				-- 本门出师
		Say("少有同门成业归来的。看入门弟子，之后再看成业行走江湖的人，不得不感叹时光飞逝啊",4,"回忆本门往事/faction","探望故友/member","我还能帮什么忙/task_get","不问了!/nothing")
	elseif (player_faction ~= "") then			-- 非本门出师、其它门派（门派不为空即指非新手）
		Say("欢迎到昆仑派，一路辛苦了，不知道有何指教/faction","了解各位道长/member","不问了!/nothing")
	else													-- 新手
		Say("昆仑派祖先道法欢迎各位参观学习",4,"Tm hiu qu?phi/faction","Tm hiu cc v?o trng/member","了解入门条件/task_get","不问了!/nothing")
	end
end

