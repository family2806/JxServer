function main()
	UTask_cy = GetTask(6)
	if ((UTask_cy == 60*256+10) and (HaveItem(237) == 1)) then				--出师任务进行中
		Talk(6,"L60_talk1","你是谁？怎敢私闯断肠谷，找死?","且慢！老伯，这里有一张丝帕，请您过目! ","丝帕？！拿来！（丝帕上有四行字）‘断肠谷中，杜鹃啼血，今昔何昔，何曾梦觉？’,你......你是谁？这块丝帕从何而来 ","晚辈是翠烟门弟子，这块丝帕是晚辈在山洞中得到的。春香婆婆指点晚辈来找您!","春香婆婆？春香那个小丫头都成了老太婆了？唉，说起来是好多年以前的事啦!")
	elseif (UTask_cy >= 60*256+20) and (UTask_cy < 70*256) and (HaveItem(235) == 0) then		--出师任务中，书信丢了
		AddEventItem(235)
		Msg2Player("获得一封关于无名老者的信. ")
		AddNote("获得一封关于无名老者的信. ")
		Talk(1,"","信也没有，你用什么来给小珍解围？")
	elseif (UTask_cy >= 60*256+20) then				-- 老者已经认识她的其它情况
		Talk(1,"","你不用劝了，我不会离开这的.")
	else
		Talk(1,"","你是谁？怎敢私闯断肠谷，找死啊?")
		Msg2Player("就像被一座大山压着，你的面前什么都没有了. ")
		Msg2Player("你被无名老者杀死了! ")
		KillPlayer()
	end
end;

