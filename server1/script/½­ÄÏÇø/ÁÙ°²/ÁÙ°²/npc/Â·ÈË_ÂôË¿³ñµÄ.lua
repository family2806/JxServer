function main()
	i = random(0,3)
	if (i == 0) then
		Talk(1,"","卖丝绸的：上好的杭绸！上好的杭绸！质薄轻柔，透风耐晒。")
	elseif (i == 1) then
		Talk(1,"","卖丝绸的：我这二的杭绸质量精美、品种繁多，绸、缎、绫、罗、锦、纺、绒、绉应有尽有，请随便挑啊！")
	elseif (i == 2) then
		Talk(1,"","卖丝绸的：看看这匹，叫做“星光缎”。你瞧，轻轻一展，犹如银河落地，群星闪烁，满屋生辉，这可是杭绸中的珍品啊！")
	else
		Talk(1,"","卖丝绸的：这匹“古香缎”，所织的鸟兽、花卉、人物神态各异，栩栩如生，雍容华贵，杭绸中的精品。怎么样，扯一匹吧？")
	end
end;
