		Talk(2, "", "我这卖的都是孙婉名家的四川刺绣. 她在成都府很有名，绣得非常好，在整个府里排名第一，她的家在城东南的芙蓉茶馆附近. ", "既然是名家刺绣，家里肯定不会缺少绣花针，也许她听说了关于魔雨针的事.")
		AddNote("在梅娘那里知道孙婉名家的四川刺绣，绣得非常好，她的家在城东南的芙蓉茶馆附近.  ")
		SetTask(2,10*256+30)
	elseif (UTask_tm == 10*256+30) then			--唐门十级任务过程中
		Talk(1, "", "我这卖的都是孙婉名家的四川刺绣. 她在成都府很有名，绣得非常好，在整个府里排名第一，她的家在城东南的芙蓉茶馆附近. ")
