		Talk(1,"","你从哪里来？这个四川刺绣是蜀中之宝，你看看，绣路完整，真是手工上品，买一个回去做纪念吧!")
	else
		Talk(1,"","喜欢芙蓉鲤鱼吗？看是不是绣得和真的一样?")
	end
end;
