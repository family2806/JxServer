function GetBonusVLKH1_main()
	if (CheckUserName() ~= 1) then
		Talk(1,"","您使用的账号不对，请检查!")
		return
	end
	------------------------------------------------------------------------------------------------------------------------------------------------
	local tbOpp = {}
	local str = "<#> 请大侠选择奖励";
	local strAccount = GetAccount()
	for k= 1, getn(tbVLKH1) do
		if (strAccount == tbVLKH1[k].szAccount) then
			local numTLL = tbVLKH1[k].nTLL
			local numKimTiger= tbVLKH1[k].nKimTiger
			local numHoaTiger = tbVLKH1[k].nHoaTiger
			local numTKCD = tbVLKH1[k].nTKCD
			local numCLBHB = tbVLKH1[k].nCLBHB
			tinsert(tbOpp,"我想领取 " .. numTLL .. " 苍狼令/#GetBonusTLL("..numTLL..")");
			
			if (numKimTiger > 0 and numKimTiger <= 8) then
				tinsert(tbOpp,"我想领取 " .. numKimTiger .. " 金星白虎王/#GetBonusKimTiger("..numKimTiger..", 1)");
			end
			
			if (numKimTiger > 8) then
				local nGetKimTiger = numKimTiger - 8
				tinsert(tbOpp,"我想领取 8  金星白虎王/#GetBonusKimTiger(8,1)");
				tinsert(tbOpp,"我想领取 " .. nGetKimTiger .. " 金星白虎王/#GetBonusKimTiger("..nGetKimTiger..", 2)");
			end
			
			if (numHoaTiger > 0 and numHoaTiger <= 8) then
				tinsert(tbOpp,"我想领取 " .. numHoaTiger .. " 火星白虎王/#GetBonusHoaTiger("..numHoaTiger..", 1)");
			end
			if (numHoaTiger > 8) then
				local nGetHoaTiger = numHoaTiger - 8
				tinsert(tbOpp,"我想领取 8  火星白虎王/#GetBonusHoaTiger(8,1)");
				tinsert(tbOpp,"我想领取 " .. nGetHoaTiger .. " 火星白虎王/#GetBonusHoaTiger("..nGetHoaTiger..", 2)");
			end
			
			
			tinsert(tbOpp,"我想领取 " .. numTKCD .. " 紫金真丹/#GetBonusTKCD("..numTKCD..")");
			tinsert(tbOpp,"我想领取 " .. numCLBHB .. " 俱乐部红包/#GetBonusCLBHBVLKH1("..numCLBHB..")");
			break
		end--ifman
	end--for
	tinsert(tbOpp, "<#>退出/OnCancel")
	if ( getn( tbOpp ) == 0 ) then
		Say(str, 0);
		return
	end;
	Say( str, getn( tbOpp ), tbOpp )
end

